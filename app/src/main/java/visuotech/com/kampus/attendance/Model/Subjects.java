package visuotech.com.kampus.attendance.Model;

public class Subjects {
    String subject_id,subject_name,subject_code,subject_sem_id,subject_department_id,subject_course_id;

    public Subjects() {
    }

    public String getSubject_id() {
        return subject_id;
    }

    public void setSubject_id(String subject_id) {
        this.subject_id = subject_id;
    }

    public String getSubject_name() {
        return subject_name;
    }

    public void setSubject_name(String subject_name) {
        this.subject_name = subject_name;
    }

    public String getSubject_code() {
        return subject_code;
    }

    public void setSubject_code(String subject_code) {
        this.subject_code = subject_code;
    }

    public String getSubject_sem_id() {
        return subject_sem_id;
    }

    public void setSubject_sem_id(String subject_sem_id) {
        this.subject_sem_id = subject_sem_id;
    }

    public String getSubject_department_id() {
        return subject_department_id;
    }

    public void setSubject_department_id(String subject_department_id) {
        this.subject_department_id = subject_department_id;
    }

    public String getSubject_course_id() {
        return subject_course_id;
    }

    public void setSubject_course_id(String subject_course_id) {
        this.subject_course_id = subject_course_id;
    }
}
